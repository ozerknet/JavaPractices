package day15_ForLoop;

public class ZPrac3 {
    public static void main(String[] args) {
        for(char i = 'a'; i <= 'z'; i++){ //i: A, B, C, D .... Z
            System.out.print(i+" ");
        }


        for(char i = 'A'; i <= 'Z'; i++){ //i: A, B, C, D .... Z
            System.out.print(i+" ");
        }


        for(char i = 'Z'; i >= 'A'; i--){ //i: A, B, C, D .... Z
            System.out.print(i+" ");
        }

        for(char i = 'z'; i >= 'a'; i--){ //i: A, B, C, D .... Z
            System.out.print(i+" ");
        }


    }
}
